        public void Update(float deltaT)
        {
            if (_init && Model != null && Shader != null)
            {
                _modelRotation += MathUtil.PiOverFour/4.0f*deltaT*(float)Settings.Rotation;

                var worldMat = Matrix.Identity;
                worldMat *= Matrix.RotationY(_modelRotation);
                
                if (Keyboard.IsKeyDown(Key.Q))
                {
                    EyeLocation += Vector3.Up * 25.0f * deltaT;
                }
                if (Keyboard.IsKeyDown(Key.E))
                {
                    EyeLocation += Vector3.Down * 25.0f * deltaT;
                }

                if (Keyboard.IsKeyDown(Key.A))
                {
                    _cameraRotation = -0.7f * deltaT;

                    float s = (float)Math.Sin(_cameraRotation);
                    float c = (float)Math.Cos(_cameraRotation);

                    var p = EyeLocation;
                    float xnew = p.X * c - p.Z * s;
                    float znew = p.X * s + p.Z * c;
                    EyeLocation = new Vector3(xnew, p.Y, znew);
                }
                if (Keyboard.IsKeyDown(Key.D))
                {
                    _cameraRotation = 0.7f * deltaT;

                    float s = (float)Math.Sin(_cameraRotation);
                    float c = (float)Math.Cos(_cameraRotation);

                    var p = EyeLocation;
                    float xnew = p.X * c - p.Z * s;
                    float znew = p.X * s + p.Z * c;
                    EyeLocation = new Vector3(xnew, p.Y, znew);
                }

                if (Keyboard.IsKeyDown(Key.W))
                {
                    EyeLocation *= (1 - 0.2f * deltaT);
                }
                else if (Keyboard.IsKeyDown(Key.S))
                {
                    EyeLocation *= (1 + 0.2f * deltaT);
                }
                
                var viewMat = Matrix.LookAtLH(EyeLocation, Vector3.Zero, Vector3.UnitY);

                //var viewMat = Matrix.LookAtLH(EyeLocation, Vector3.Zero, Vector3.UnitY);
                var projMat = Matrix.PerspectiveFovLH(MathUtil.PiOverFour, (float)_renderControl.ActualWidth / (float)_renderControl.ActualHeight, 0.1f, 1000f);
                var viewInv = new Matrix(viewMat.ToArray());
                viewInv.Invert();

                Shader.SetWorld(worldMat);
                Shader.SetWorldViewProjection(worldMat*viewMat*projMat);
                Shader.SetViewProjection(viewMat * projMat);
                Shader.SetViewInverse(viewInv);
                
                Shader.SetVariables(Settings);
            }
        }

        public void Render(float deltaT)
        {
            if (MyDevice == null || !_init)
                return;

            if (Model != null && Shader != null)
            {
                MyDevice.InputAssembler.InputLayout = Shader.InputLayout;
                MyDevice.InputAssembler.PrimitiveTopology = Model.PrimitiveTopology;
                //MyDevice.InputAssembler.SetIndexBuffer(Model.IndexBuffer, Format.R32_UInt, 0); //No need indices, points
                MyDevice.InputAssembler.SetVertexBuffers(0, new VertexBufferBinding(Model.VertexBuffer, Model.VertexStride, 0));

                for (int i = 0; i < Shader.Technique.Description.PassCount; ++i)
                {
                    Shader.Technique.GetPassByIndex(i).Apply();
#region CBUFFER
                    //Device.GeometryShader.SetConstantBuffers(1, 2, _buffers); //cbuffer
#endregion

                    // GS Output
                    _streamOutputBinding = new[] { new StreamOutputBufferBinding(_streamOutput, 0) };
                    MyDevice.StreamOutput.SetTargets(_streamOutputBinding);

                    MyDevice.Draw(Model.VertexCount, 0);

                    MyDevice.CopyResource(_streamOutput, _stagingBuffer);
                    using (DataStream stream = _stagingBuffer.Map(MapMode.Read, MapFlags.None))
                    {
                        GSData = stream.ReadRange<GS4>(GSData.Length);
                    }
                    _stagingBuffer.Unmap();
                }

            }
